
DRAW_THREE_HORIZONTAL_LINES:
	LD R[0] 10
	LD R[1] 100
	LD R[2] 20
	LD R[3] 0XAC80FFFF
	CALL DRAW_HORIZONTAL_LINE

	LD R[0] 1
	LD R[1] 10
	LD R[2] 2
	LD R[3] 0XFF80FFFF
	CALL DRAW_HORIZONTAL_LINE

	LD R[0] 7
	LD R[1] 7
	LD R[2] 8
	LD R[3] 0X0000FFFF
	CALL DRAW_HORIZONTAL_LINE

	CALL STD_STD_SCREEN_FILL_RED


// Draws a line (X0,Y) to (X1, Y)
// args: X0, Y, X1, color R[0:3]
DRAW_HORIZONTAL_LINE:
	CALL STD_VRAM_INDEX_FROM_X_Y
	LD R[4096] R[4100]

	// todo - only works when x1 > x0
	LD R[4097] R[4100]
	ADD R[4097] R[2]

	LD R[U:V] R[3]
	BLIT

	RETURN
