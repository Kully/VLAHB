DRAW_INFINITE_LINE_FROM_TOP_LEFT:
	// load indices in U,V
	LD R[4096] 4100
	LD R[4097] 4260

	// store x=0 and y=0 far from VLAHB
	LD R[63000] 0
	LD R[63001] 0

		MAIN_DRAW_INFINITE_LINE_FROM_TOP_LEFT:
		// load (x,y) in U
		LD R[0] R[63000]
		LD R[1] R[63001]
		CALL VRAM_INDEX_FROM_X_Y
		LD R[4096] R[4100]

		LD R[U] 0XAC80FFFF
		BLIT

		// increment x and y
		ADD R[63000] 1
		ADD R[63001] 1

		GOTO MAIN_DRAW_INFINITE_LINE_FROM_TOP_LEFT
