// slots for display: R[4100:43200]

LOVE_AND_DRAW:
	LD R[19998] 0XFF00FFFF
	LD R[5100:5200] R[19998]
	BLIT

	LD R[5200:5300] R[5100:5200]
	BLIT

	LD R[5300:5400] R[5200:5300]
	BLIT

	CALL BLACK_SCREEN_CLEAR

	GOTO LOVE_AND_DRAW


BLACK_SCREEN_CLEAR:
	LD R[9996] 0X000000FF
	LD R[4100:43200] R[9996]
	BLIT
	RETURN

EXIT
